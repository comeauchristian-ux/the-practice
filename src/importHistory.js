function parseNum(raw){
  if(raw === '' || raw == null) return null
  const n = Number(raw)
  return Number.isFinite(n) ? n : null
}

function normalizeState(raw){
  if(raw === 'contested') return 'contested'
  return 'inevitable'
}

export function buildImportedLogs(){
  const logs = {}
  const tsBase = Date.UTC(2026, 0, 1, 0, 0, 0)

  for(let i=0; i<IMPORT_HISTORY_ROWS.length; i++){
    const [date, dayKey, exerciseId, weightRaw, repsRaw, inclineRaw, speedRaw, stateRaw, noteRaw] = IMPORT_HISTORY_ROWS[i]
    if(!date || !dayKey || !exerciseId) continue

    if(!logs[date]) logs[date] = {}
    if(!logs[date][dayKey]) logs[date][dayKey] = {}

    const entry = {
      state: normalizeState(stateRaw),
      updatedAt: tsBase + i,
    }

    const weight = parseNum(weightRaw)
    const reps = parseNum(repsRaw)
    const incline = parseNum(inclineRaw)
    const speed = parseNum(speedRaw)
    const note = (noteRaw || '').trim()

    if(weight != null) entry.weight = weight
    if(reps != null) entry.reps = reps
    if(incline != null) entry.incline = incline
    if(speed != null) entry.speed = speed
    if(note) entry.note = note

    logs[date][dayKey][exerciseId] = entry
  }

  return logs
}
